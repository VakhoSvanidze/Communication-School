function averageAge(data) {
  let total = data.reduce((total, current) => {
    return total + current.age;
  }, 0)
  let average = Math.floor(total / data.length);
  return average;
}

function moreThen25(data) {
  const array = [];
  data.forEach(person => {
    let age = person.age;
    if(age > 25) {
      array.push(person.firstname);
    }
  })
  return array;
}


function emailCom(data) {
  const array = [];
  data.forEach(person => {
    const email = person.email;
    const splittedArray = email.split(".");
    if(splittedArray[splittedArray.length - 1] === "com"){
      array.push(person.lastname);
    }
  })
  return array;
}

function sexCount(data) {
  const sexes = {
    male: 0,
    female: 0
  }
  data.forEach(person => {
    if(person.sex === "Male") {
      sexes.male++;
    } else {
      sexes.female++;
    }
  })
  return `Male: ${sexes.male}, Female: ${sexes.female}`;
}

